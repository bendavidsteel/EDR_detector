
public class SCR {

	long peak_time;
	double peak_eda;
	long rise_time;
	double max_deriv;
	double amplitude;
	
	public SCR(long peak_time, double peak_eda, long rise_time, double max_deriv, double amplitude) {
		this.peak_time = peak_time;
		this.peak_eda = peak_eda;
		this.rise_time = rise_time;
		this.max_deriv = max_deriv;
		this.amplitude = amplitude;
	}
	
}
